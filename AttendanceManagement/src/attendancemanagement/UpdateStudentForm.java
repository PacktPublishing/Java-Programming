public void bind2(){
       try{
           String srollno=txtrollno.getText();
           String p="Select StudentFirstName,StudentLastName, StudentAddress, StudentContactNo,StudentParentName from StudentDetails where StudRollNo='"+srollno+"'";
            connect();
            pst=con.prepareStatement(p);
            rs=pst.executeQuery();
            while(rs.next()){
                txtfname.setText(rs.getString("StudentFirstName"));
                txtlname.setText(rs.getString("StudentLastName"));
                txtadd.setText(rs.getString("StudentAddress"));
                txtcontactno.setText(rs.getString("StudentContactNo"));
                txtpname.setText(rs.getString("StudentParentName"));
            }
            txtadd.setEditable(true);
            txtcontactno.setEditable(true);
            txtfname.setEditable(true);
            txtlname.setEditable(true);
            txtpname.setEditable(true);
            
       }catch(Exception ex){
           JOptionPane.showMessageDialog(this, ex.toString());
       }
   }
